Angry Salamander is a frontal column AoE attack that deals a moderate amount of damage to all targets hit.
Aglaia Climb is a frontal attack that hits targets very rapidly.
Order to Attack summons 7th Cohort Secutors.
Adds
7th Cohort Secutor is a melee damage dealer summoned by Livia in groups.
